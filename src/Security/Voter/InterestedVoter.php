namespace App\Security\Voter;

use App\Entity\Interested;
use App\Entity\User;
use DateTime;
use PhpParser\Node\Stmt\Return_;
use Symfony\Component\Security\Core\Authentication\Token\TokenInterface;
use Symfony\Component\Security\Core\Authorization\AuthorizationCheckerInterface;
use Symfony\Component\Security\Core\Authorization\Voter\Voter;
use Symfony\Component\Security\Core\User\UserInterface;

class InterestedVoter extends Voter
{
    public const EDIT = 'edit';
    public const DELETE = 'delete';

    private AuthorizationCheckerInterface $authorization;

    public function __construct(AuthorizationCheckerInterface $authorization)
    {
        $this->authorization = $authorization;
    }

    protected function supports($attribute, $subject)
    {
        // replace with your own logic
        // https://symfony.com/doc/current/security/voters.html
        return in_array($attribute, [self::EDIT, self::DELETE])
            && $subject instanceof \App\Entity\Interested;
    }

    protected function voteOnAttribute($attribute, $subject, TokenInterface $token)
    {
        $user = $token->getUser();
        // if the user is anonymous, do not grant access
        if (!$user instanceof UserInterface) {
            return false;
        }
        assert($user instanceof User);

        switch ($attribute) {
            case self::EDIT:
                return $this->canEdit($subject, $user);
            case self::DELETE:
                return $this->canDelete($subject, $user);
        }

        return false;
    }

    private function canEdit(Interested $interested, User $user): bool
    {
        return ($interested->getUser() === $user && !$user->isDisabled());
    }

    private function canDelete(Interested $interested, User $user): bool
    {
        if ($this->canEdit($interested, $user)) {
            if ($interested->getSharable()->getInterestedMethod() === 4) {
                return true;
            }
            if ($interested->getSharable()->getInterestedMethod() === 3) {
                return !$interested->getReviewed();
            }
        }
        return false;
    }
}
